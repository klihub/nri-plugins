// ExtractTarArchive extracts the given archive to the given directory.
func ExtractTarArchive(file, dir string) error {
	var (
		tr *tar.Reader
	)

	err := os.MkdirAll(dir, os.FileMode(0755))
	if err != nil {
		return fmt.Errorf("failed to create directory %s: %v", dir, err)
	}

	fr, err := os.Open(file)
	if err != nil {
		return fmt.Errorf("failed to open archive %s: %w", file, err)
	}
	defer fr.Close()

	switch path.Ext(file) {
	case ".gz":
		gzr, err := gzip.NewReader(fr)
		if err != nil {
			return fmt.Errorf("failed to uncompress archive %s: %w", file, err)
		}
		defer gzr.Close()
		tr = tar.NewReader(gzr)

	case ".bz2":
		bzr := bzip2.NewReader(fr)
		if err != nil {
			return fmt.Errorf("failed to uncompress archive %s: %w", file, err)
		}
		tr = tar.NewReader(bzr)
	}

	patchPerms := map[string]fs.FileMode{}

	for {
		hdr, err := tr.Next()
		switch {
		case err == io.EOF:
			return nil
		case err != nil:
			return fmt.Errorf("failed to extract archive %s: %w", file, err)
		case hdr == nil:
			continue
		}

		dst := filepath.Join(dir, hdr.Name)

		switch hdr.Typeflag {
		case tar.TypeDir:
			if (hdr.Mode & 0300) != 0300 {
				patchPerms[dst] = fs.FileMode(hdr.Mode)
			}
			err := os.MkdirAll(dst, os.FileMode(hdr.Mode|0300))
			if err != nil {
				return fmt.Errorf("failed to create dir %s for archive %s: %w",
					dst, file, err)
			}

		case tar.TypeReg:
			f, err := os.OpenFile(dst, os.O_CREATE|os.O_WRONLY, os.FileMode(hdr.Mode))
			if err != nil {
				return fmt.Errorf("failed to create file %s for archive %s: %w",
					dst, file, err)
			}
			_, err = io.Copy(f, tr)
			f.Close()
			if err != nil {
				return fmt.Errorf("failed to write file %s for archive %s: %w",
					dst, file, err)
			}

		case tar.TypeLink:
			err := os.Link(hdr.Linkname, dst)
			if err != nil {
				return fmt.Errorf("failed create link %s (%s) for archive %s: %w",
					dst, hdr.Linkname, file, err)
			}

		case tar.TypeSymlink:
			err := os.Symlink(hdr.Linkname, dst)
			if err != nil {
				return fmt.Errorf("failed create symlink %s (%s) for archive %s: %w",
					dst, hdr.Linkname, file, err)
			}
		}
	}

	for path, perm := range patchPerms {
		_ = os.Chmod(path, perm)
	}

	return nil
}
