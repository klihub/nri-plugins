type (
	ID       = sysfs.ID
	MemInfo  = sysfs.MemInfo
	NUMAStat = sysfs.NUMAStat
)

type testSystem struct {
	tarball string
	nodes   *Nodes
	cpus    *CPUs
}

type Nodes struct {
	hasCPU          []ID
	hasMemory       []ID
	hasNormalMemory []ID
	online          []ID
	possible        []ID
	nodes           map[ID]*Node
}

type Node struct {
	id       ID
	cpuset   string
	distance []int
	memInfo  *MemInfo
	numaStat *NUMAStat
}

type CPUs struct {
	present  string
	online   string
	offline  string
	isolated string
	cpus     map[ID]*CPU
}

type (
	CPUFreq = sysfs.CPUFreq
)

